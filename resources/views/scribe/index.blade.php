        <h1 id="introduction-wallet-tracker-api">Introduction Wallet Tracker API</h1>
<aside>
    <strong>Base URL</strong>: <code>https://my-laravel-site.ddev.site</code>
</aside>
<pre><code>This documentation aims to provide all the information you need to work with our API.

&lt;aside&gt;As you scroll, you'll see code examples for working with the API in different programming languages in the dark area to the right (or as part of the content on mobile).
You can switch the language used with the tabs at the top right (or from the nav menu at the top left on mobile).&lt;/aside&gt;</code></pre>
<p>Welcome to the Wallet Tracker API documentation.<br />
This API allows you to manage <strong>financial wallets</strong>, <strong>transactions</strong>, and <strong>categories</strong> in a structured and RESTful way.</p>
<h3 id="features">Features</h3>
<ul>
<li>Create, view, update, and delete wallets</li>
<li>Track income/expense transactions</li>
<li>Organize transactions with categories</li>
<li>Filter results using query parameters</li>
<li>Fully documented with real-time testing (Try it out)</li>
</ul>
<blockquote>
<p>Note: This API currently does <strong>not require authentication</strong>.</p>
</blockquote>
<p>For each endpoint, you’ll find:</p>
<ul>
<li>Example requests and responses</li>
<li>Required parameters</li>
<li>Description of purpose</li>
<li>Live “Try it out” functionality</li>
</ul>
<p>Enjoy using the API — feel free to test all endpoints directly from this documentation!</p>

